package me.zhouzhuo810.zznote.event;

public class SelectionChangeEvent {
    private int start;
    private int end;

    public SelectionChangeEvent(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
